			<p><h3>Дежурный инструктор и Персональный тренер :</h3></p>
			
			<p><b class="word">Дежурный инструктор</b> - это инструктор тренажерного зала или персональный тренер, который всегда находится в зале в течение определенного интервала времени (дежурства), и, в случае необходимости, он может подстраховать при выполнении упражнений с большим весом, помочь настроить тренажер или показать способы его использования. Также дежурный инструктор всегда проводит первичный инструктаж для новичков.			</p><p>
Что такое первичный (вводный) инструктаж?
Первичный (вводный) инструктаж - это процедура, проводимая дежурным инструктором со всеми клиентами, пришедшими в зал в первый раз. На этом инструктаже дежурный инструктор знакомит клиентов с оборудованием, имеющимся в тренажерном зале, со способами и правилами его использования, дополнительными услугами. Длительность инструктажа — 50–55 минут.
<b class="word">Важно знать!</b></p>
			
			<p><b class="word">Персональный тренер</b> - специалист не только по силовому тренингу, но и по аэробному тренингу, стретчингу, разбирается в вопросах правильного питания в зависимости от нагрузок, отлично разбирается в методиках снижения веса и набора мышечной массы.</p><br /><br />
